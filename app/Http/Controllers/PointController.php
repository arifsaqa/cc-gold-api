    public function use(Request $request)
    {
        $point = Point::where('userId', '=', $request->userId)->first();
        if (!$point->point >= 1) {
            return response()->json([
                'status' => 0,
                'message' => 'Point anda kurang'
            ]);
        }
        $point->point -= 1;
        $point->save();
        return response()->json([
            'status' => 1,
            'data' => $point,
            'message' => 'Penukaran point berhasil!'
        ]);
    }
